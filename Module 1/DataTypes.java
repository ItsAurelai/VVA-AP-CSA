public class DataTypes
{
    public DataTypes()
    {
        // Part I: Declare Variables
        int integerVar = 3;
        double doubleVar = 6.5;
        boolean booleanVar = true;
        String stringVar = "Hello";
        
        //Part II: Print Each Variable
        System.out.println("Integer Variable: " + integerVar);
        System.out.println("Double Variable: " + doubleVar);
        System.out.println("Boolean Variable: " + booleanVar);
        System.out.println("String Variable: " + stringVar);
        
        //Part III: Update Variables
        integerVar = 6;
        doubleVar = 1.2;
        booleanVar = false;
        stringVar = "Goodbye";
        
        //Part IV: Print Updated Variables
        System.out.println("Updated Integer Variable: " + integerVar);
	System.out.println("Updated Double Variable: " + doubleVar);
	System.out.println("Updated Boolean Variable: " + booleanVar);
        System.out.println("Updated String Variable: " + stringVar);

    }
}
